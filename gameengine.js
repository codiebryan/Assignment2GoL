GameEngine.prototype.init = function (ctx) {
    this.ctx = ctx;
    this.surfaceWidth = this.ctx.canvas.width;
    this.surfaceHeight = this.ctx.canvas.height;
    this.timer = new Timer();
    console.log('game initialized');
}

GameEngine.prototype.start = function () {
    console.log("starting game");
    var that = this;
    (function gameLoop() {
        that.loop();
        requestAnimFrame(gameLoop, that.ctx.canvas);
    })();
}

GameEngine.prototype.addEntity = function (entity) {
    this.entities.push(entity);
}

GameEngine.prototype.draw = function () {
    this.ctx.clearRect(0, 0, this.surfaceWidth, this.surfaceHeight);
    this.ctx.save();
    for (var i = 0; i < this.grid.length; i++) {
        for (var j = 0; j < this.grid[i].length; j++) {
            for (var n = 0; n < this.entities.length; n++) {
                if (this.entities[n].i == i && this.entities[n].j == j) {
                    this.entities[n].alive = this.grid[i][j];
                    this.entities[n].draw(this.ctx);
                }
            }
        }
    }
    this.ctx.font = "12px Arial";
    this.ctx.fillStyle = "white";
    this.ctx.fillText("Generation: " + this.generation,360,10);
    this.ctx.fillText("Live cells: " + this.livecells,360,25);
    this.ctx.restore();
}

